const addStockReportTable = (doc, data, yPosition) => {
    if (!data.inventory || data.inventory.length === 0) {
        doc.text('No stock data available', 20, yPosition);
        return yPosition + 20;
    }

    const tableData = data.inventory.map(item => [
        item.name || '-',
        item.category?.name || '-',
        item.brand?.name || '-',
        item.stock || 0,
        `Rs. ${item.sellingRate?.toFixed(2) || '0.00'}`,
        `Rs. ${(item.stock * item.sellingRate)?.toFixed(2) || '0.00'}`
    ]);

    doc.autoTable({
        head: [['Item Name', 'Category', 'Brand', 'Stock', 'Rate', 'Value']],
        body: tableData,
        startY: yPosition,
        theme: 'grid',
        styles: { fontSize: 8 },
        headStyles: { fillColor: [155, 89, 182] }
    });

    return doc.lastAutoTable.finalY + 20;
};

